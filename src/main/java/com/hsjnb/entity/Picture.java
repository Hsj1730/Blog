@Entity
@Table(name = "t_picture")
public class Picture {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    private String pictureName; //图片名称

    private String pictureTimeLocation; //时间地点

    private String pictureAddress;  //图片地址

    private String pictureDescription;  //图片描述

    public Picture() {

    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getPictureName() {
        return pictureName;
    }

    public void setPictureName(String pictureName) {
        this.pictureName = pictureName;
    }

    public String getPictureTimeLocation() {
        return pictureTimeLocation;
    }

    public void setPictureTimeLocation(String pictureTimeLocation) {
        this.pictureTimeLocation = pictureTimeLocation;
    }

    public String getPictureAddress() {
        return pictureAddress;
    }

    public void setPictureAddress(String pictureAddress) {
        this.pictureAddress = pictureAddress;
    }

    public String getPictureDescription() {
        return pictureDescription;
    }

    public void setPictureDescription(String pictureDescription) {
        this.pictureDescription = pictureDescription;
    }

    @Override
    public String toString() {
        return "Picture{" +
                "id=" + id +
                ", pictureName='" + pictureName + '\'' +
                ", pictureTimeLocation='" + pictureTimeLocation + '\'' +
                ", pictureAddress='" + pictureAddress + '\'' +
                ", pictureDescription='" + pictureDescription + '\'' +
                '}';
    }
}
